class Map {
  constructor() {
    this.position = this.position;
  }

  keyboard_event() {}
  update() {}
  draw(delta, ctx) {
    /* Fill the code */
    const totalRatio = 1024 / pacmanMap.length;
    //ctx.save();
    for (let y = 0; y < pacmanMap.length; y++) {
      // en el caso de querer ajustar la linea horizontal al canvas
      //let horizontalSize = 1024 / pacmanMap[y].length;

      for (let x = 0; x < pacmanMap[y].length; x++) {
        ctx.beginPath();
        const mapCharacter = pacmanMap[y][x];
        if (mapCharacter === "W") {
          ctx.fillStyle = "green";
          ctx.rect(
            x * totalRatio, // x * horizontalSize
            y * totalRatio,
            totalRatio,
            totalRatio
          );
        }
        if (mapCharacter == ".") {
          ctx.fillStyle = "grey";
          ctx.rect(
            x * totalRatio, // x * horizontalSize
            y * totalRatio,
            totalRatio,
            totalRatio
          );
        }
        ctx.closePath();
        ctx.fill();
        ctx.stroke();
      }
    }
  }
}
